        public static void GetPathes(int[,] maze)
        {
            for (int i = 0; i < size; i++)
            {
                for (int j = 0; j < size; j++)
                {
                    if (maze[i, j] != 1)
                    {
                        FreePath.Add(new int[] { i, j });
                    }
                }
            }
        }
        public static int[] GetRandomPath()
